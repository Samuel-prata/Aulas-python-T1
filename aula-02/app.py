
verdade = 'pyThOn é iNCrível'

# lower () -> retornar a varíavel em letras minusculas
print(verdade.lower())
# upper () -> retornar a varíavel em letras maiusculas
print(verdade.upper())
# capitalize() -> Retorna a variável com a inicial em maiuscula
print(verdade.capitalize())
# replace (antiga, nova) ->Método que substitui a palavra antiga pela nova palavras
print(verdade.replace('pyThOn', 'Javascript'))
# split ()  -> método que separa a string em arrays (listas)
print(verdade.split(' '))
print(verdade.islower())

